package com.example.gameguide;

public class Guide
{
	private String guideTitle;
	
	private String url;

	public String getGuideTitle()
	{
		return guideTitle;
	}

	public void setGuideTitle(String guideTitle)
	{
		this.guideTitle = guideTitle;
	}

	public String getUrl()
	{
		return url;
	}

	public void setUrl(String url)
	{
		this.url = url;
	}
}
